// 获取线性图标的函数
export const getLinearIcon = (iconKey: string): React.ReactElement | null => {
    const icon = LinearIcons[iconKey as keyof typeof LinearIcons];
    if (!icon) return null;

    // 为图标添加统一的灰色
    return React.cloneElement(icon, {
        style: { color: '#8c8c8c' }
    });
};

// 获取小尺寸的线性图标（12px）
export const getSmallLinearIcon = (iconKey: string): React.ReactElement | null => {
    const icon = LinearIcons[iconKey as keyof typeof LinearIcons];
    if (!icon) return null;

    // 克隆图标并修改尺寸和颜色
    return React.cloneElement(icon, {
        width: "12",
        height: "12",
        style: { color: '#8c8c8c' }
    });
};

// 获取按钮图标（继承按钮文本颜色）
export const getButtonIcon = (iconKey: string): React.ReactElement | null => {
    const icon = LinearIcons[iconKey as keyof typeof LinearIcons];
    if (!icon) return null;

    // 克隆图标，使用 currentColor 让它继承父元素的文本颜色
    return React.cloneElement(icon, {
        style: { color: 'currentColor' }
    });
};

// 获取所有可用图标的键名
export const getAllIconKeys = (): string[] => {
    return Object.keys(LinearIcons);
};

// 获取图标前缀的通用函数
export const getIconPrefix = (iconKey?: string): React.ReactElement => {
    if (iconKey) {
        const icon = getLinearIcon(iconKey);
        if (icon) {
            return <span style={{
                opacity: 1,
                display: 'inline-flex',
                alignItems: 'center',
                color: '#8c8c8c'
            }}>{icon}</span>;
        }
    }
    // 返回透明的占位符，保持DOM结构稳定
    return <span style={{ opacity: 0, width: '0px' }}></span>;
};
